// output
console.log("Hello JavaScript");

// variables
// let itu untuk mendeklarasi variabel yang nilainya bisa berubah
// const itu untuk mendeklarasi variabel yang nilainya tidak bisa berubah
let name = "Vanya";
const age = 20;

// name dan age ini adalah parameter
console.log(name, age);

// data types
// type of untuk mengetahui tipe data dari sebuah variabel
let isStudent = true;
let score = 90;
let grade = "A";
let phi = 3.1456789;

console.log(typeof name); // string
console.log(typeof isStudent); // boolean
console.log(typeof score); // number
console.log(typeof grade); // string
console.log(typeof phi); // number
